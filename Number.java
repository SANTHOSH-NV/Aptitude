class Numbers
{
	public static void main(String a[])
	{
		int q = 10;
		for(int i=0;i<=q;i++)
		{
			System.out.println("The number is; " + i);
		}
		int r=0;
		while(10>=r)
		{
		System.out.println("The number is; " + r);
		r++;
		}
		int t=1;
		do{
			System.out.println("The number is; " +t );
			t++;
		}while(t<=10);
	}
}