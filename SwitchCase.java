class SwitchCase
{
public static void main(String a[])
     {
	 int num = 5;
	 switch (num)
	 {
	 case 1:
	 System.out.println("case 1 is executed");
	 break;
	 case 2:
	 System.out.println("case 2 is executed");
	 break;
	 default:
	 System.out.println("default case is executed");
	 break;
	 }
	 }
}
	 